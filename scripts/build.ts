// 1. Resolve deprecated-flag warnings and decide which opt-in targets build this run.
const determineBuildTargets = (): { buildGemini: boolean; buildCodex: boolean } => {
  for (const flag of DEPRECATED_BUILD_FLAGS) {
    if (args.has(flag)) {
      console.error(
        `[build] ${flag} is deprecated and now a no-op — every git-tracked build target is ` +
          'regenerated by default (ADR-007 T2). Scheduled for removal next release.'
      );
    }
  }

  const geminiTracked = isTargetTracked(root, GEMINI_TARGET_DIRS);
  const codexTracked = isTargetTracked(root, CODEX_TARGET_DIRS);
  if (!geminiTracked) {
    console.error(
      '[build] gemini build targets are not tracked in git yet — pass --gemini or --all to opt in for this run.'
    );
  }
  if (!codexTracked) {
    console.error(
      '[build] codex build targets are not tracked in git yet — omit --no-codex (or pass --all) to opt in for this run.'
    );
  }

  // Tracked ⇒ built by default (ADR-007 T2/R5′). The opt-in flags only still matter for a
  // genuinely untracked target — once tracked, the flags below are inert no-ops (deprecation
  // notice printed above).
  const buildGemini = geminiTracked || args.has('--gemini') || args.has('--all');
  const buildCodex = codexTracked || !args.has('--no-codex');
  return { buildGemini, buildCodex };
};

const cleanBuildDirectories = (buildGemini: boolean, buildCodex: boolean) => {
  console.log('Cleaning existing build directories...');
  cleanDir(path.join(root, 'rules'));
  cleanDir(path.join(root, 'agents'));
  cleanDir(path.join(root, 'skills'));
  cleanDir(path.join(root, 'references'));
  cleanDir(path.join(root, '.cursor'));
  // Only clean the subdirs build.ts actually writes — `.claude/` also holds
  // maintainer state (initiatives/, progress.md) that must survive a rebuild.
  // `.claude/skills` is narrowed to the `blackhole/` subdir specifically (issue #267): build only
  // ever writes `.claude/skills/blackhole/`, so wiping the whole `skills/` dir deleted sibling
  // maintainer-only skills (e.g. `create-release/`) that live alongside it. `.claude/agents` and
  // `.claude/rules` still wipe wholesale — narrow them the same way only if maintainer agents/rules
  // are ever added (YAGNI; not done now, since build writes the entirety of both dirs today).
  cleanDir(path.join(root, '.claude', 'agents'));
  cleanDir(path.join(root, '.claude', 'rules'));
  cleanDir(path.join(root, '.claude', 'skills', 'blackhole'));
  cleanDir(path.join(root, '.claude-plugin'));
  // Claude marketplace distribution bundle (ADR-009) — unconditional like the rest of Target C
  // above, not gated behind the Gemini/Codex tracked-target opt-in flags: it is the redistributable
  // form of the always-built repo-root .claude/ Target C, not an optional bonus target.
  cleanDir(path.join(root, CLAUDE_DISTRIBUTION_ROOT));
  if (buildGemini) {
    cleanDir(path.join(root, AGENTS_BUILD_ROOT));
    cleanDir(path.join(root, '.gemini-plugin'));
    cleanDir(path.join(root, DISTRIBUTION_ROOT));
  }
  if (buildCodex) {
    cleanDir(path.join(root, 'codex-agents'));
    cleanDir(path.join(root, 'codex-skills'));
    cleanDir(path.join(root, '.codex-plugin'));
  }
  if (fs.existsSync(path.join(root, 'SKILL.md'))) {
    fs.unlinkSync(path.join(root, 'SKILL.md'));
  }
  if (buildCodex && fs.existsSync(path.join(root, 'codex-marketplace.json'))) {
    fs.unlinkSync(path.join(root, 'codex-marketplace.json'));
  }
};

