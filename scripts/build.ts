/**
 * Step-0 tracking audit: does at least one of `paths` (relative to `rootDir`) contain a
 * git-tracked file? Used to decide whether a build target's opt-in flag can retire (tracked)
 * or must survive (genuinely untracked) — never collapsed onto a hardcoded assumption.
 */
export function isTargetTracked(rootDir: string, paths: string[]): boolean {
  for (const p of paths) {
    try {
      const out = execFileSync('git', ['ls-files', '--', p], { cwd: rootDir, encoding: 'utf-8' });
      if (out.trim().length > 0) return true;
    } catch {
      // git invocation failed (e.g. path outside a repo) — treat as untracked, not an error.
    }
  }
  return false;
}

